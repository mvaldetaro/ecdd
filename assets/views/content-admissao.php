                <li>Mediante a apresentação de toda a documentação exigida</li>
                <li>Mediante existência de vaga no curso/turno pretendido</li>
              </ol>
              <p>As transferências externas somente serão válidas para cursos de áreas afins, ou seja, vinculados à mesma área do conhecimento.</p>
